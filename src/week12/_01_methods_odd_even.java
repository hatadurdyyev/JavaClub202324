package week12;

public class _01_methods_odd_even {

/*
    Ask a user to enter integer numbers then create a function that determines whether odd or even number
*/
    public static void fdsgfd(int num){
        if (num % 2 == 0){
            System.out.println("Even");
        }
        else {
            System.out.println("Odd");
        }
    }

    public static void main(String[] args) {
        fdsgfd(6);
    }
}
